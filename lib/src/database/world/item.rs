macro_rules! item_generator {
    (
        $(
            $item:ident => $tag:ident
        )*
    ) => {
        #[derive(Debug, Clone, Copy, PartialEq, Eq)]
        pub enum Item {
            $(
                $item,
            )*
        }

        impl Item {
            pub fn tag(&self) -> &'static str {
                match self {
                    $(
                        Item::$item => stringify!($tag),
                    )*
                }
            }

            pub fn from_tag(tag: &str) -> Option<Item> {
                match tag {
                    $(
                        stringify!($tag) => Some(Item::$item),
                    )*
                    _ => None,
                }
            }
        }

        impl serde::Serialize for Item {
            fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error>
            where
                S: serde::Serializer,
            {
                serializer.serialize_str(self.tag())
            }
        }

        impl<'de> serde::Deserialize<'de> for Item {
            fn deserialize<D>(deserializer: D) -> Result<Self, D::Error>
            where
                D: serde::Deserializer<'de> {
                Item::from_tag(&String::deserialize(deserializer)?).ok_or_else(|| serde::de::Error::custom("unknown item tag"))
            }
        }
    };

}

